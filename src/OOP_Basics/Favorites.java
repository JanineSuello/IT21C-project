
package OOP_Basics;


class Favorites {
    String name;
    String hobbies;
    
    Favorites(String name, String hobbies) {
        this.name = name;
        this.hobbies = hobbies;
  
    }
    String getName() {
        return name;
    }
    String getHobbies() {
        return hobbies;
    }
}
